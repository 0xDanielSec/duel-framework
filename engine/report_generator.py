# Detection-rate bar — drawn inline inside table cells
# ---------------------------------------------------------------------------

class _RateBar(Flowable):
    def __init__(self, rate: float, width: float = 72, height: float = 9):
        super().__init__()
        self.rate  = max(0.0, min(1.0, rate))
        self.width = width
        self.height = height

    def wrap(self, *_):
        return self.width, self.height

    def draw(self):
        c     = self.canv
        fill  = self.width * self.rate
        color = _C["blue"] if self.rate >= 0.5 else _C["red"]
        c.setFillColor(_C["bar_bg"])
        c.rect(0, 0, self.width, self.height, fill=1, stroke=0)
        if fill > 0:
            c.setFillColor(color)
            c.rect(0, 0, fill, self.height, fill=1, stroke=0)
        c.setFont("Helvetica-Bold", 7)
        c.setFillColor(white if self.rate >= 0.35 else _C["text"])
        c.drawCentredString(self.width / 2, 1.5, f"{self.rate:.0%}")


