    # ------------------------------------------------------------------
    # Page 3 — Attacker Mutation Analysis
    # ------------------------------------------------------------------

    def _page_mutations(self) -> list:
        ST = self.ST
        story = _section_header(
            "ATTACKER MUTATION ANALYSIS",
            "How the Attacker adapted across rounds to evade detection",
        )

        if len(self.scorer.rounds) < 2:
            story.append(_p("At least 2 rounds are required for mutation analysis.", ST["body"]))
            return story

        stable, rotating = self.analyst._classify_fields()

        # Stable signatures
        story.append(Paragraph("Stable Attack Signatures", ST["section"]))
        story.append(_p(
            "Fields that held the same value in every round — "
            "the highest-confidence IOCs the Attacker did not rotate.", ST["body"]))

        if stable:
            hdr = ["FIELD", "STABLE VALUE(S)", "ROUNDS PRESENT"]
            rows = [hdr]
            for field, values in sorted(stable.items()):
                vals = ", ".join(sorted(str(v) for v in values)[:5])
                if len(values) > 5:
                    vals += f"  +{len(values)-5} more"
                rows.append([field, vals, f"all {len(self.scorer.rounds)}"])
            story.append(_col_table(rows, [45*mm, _CONTENT_W - 90*mm, 45*mm], small=True))
        else:
            story.append(_p("The Attacker rotated all tracked fields — no stable signatures.", ST["body"]))

        # Mutation vectors
        story.append(Paragraph("Mutation Vectors", ST["section"]))
        story.append(_p(
            "Fields that changed between rounds. [reactive] marks fields the Defender "
            "targeted in the previous round's KQL rule — indicating the Attacker "
            "read and evaded the rule.", ST["body"]))
        story.append(Spacer(1, 2 * mm))

        ev = self.analyst._evaded_values
        kf = self.analyst._kql_fields
        any_delta = False
        for i in range(1, len(self.scorer.rounds)):
            prev_r = self.scorer.rounds[i - 1]["round"]
            curr_r = self.scorer.rounds[i]["round"]
            prev_kql_fields = kf.get(prev_r, set())
            deltas = []
            for field in rotating:
                prev_v = ev.get(prev_r, {}).get(field, set())
                curr_v = ev.get(curr_r, {}).get(field, set())
                added, removed = curr_v - prev_v, prev_v - curr_v
                if added or removed:
                    reactive = " [reactive]" if field in prev_kql_fields else ""
                    parts = []
                    if removed:
                        parts.append("dropped " + ", ".join(str(v) for v in sorted(removed)[:3]))
                    if added:
                        parts.append("added " + ", ".join(str(v) for v in sorted(added)[:3]))
                    deltas.append([field + reactive, "; ".join(parts)])
            if deltas:
                any_delta = True
                block_title = _p(f"Round {prev_r} → Round {curr_r}", ST["cell_b"])
                hdr = ["FIELD", "CHANGE"]
                cw  = [50*mm, _CONTENT_W - 50*mm]
                t   = _col_table([hdr] + deltas, cw, small=True)
                story.append(KeepTogether([block_title, Spacer(1, 1*mm), t, Spacer(1, 3*mm)]))

        if not any_delta:
            story.append(_p("No field-level mutations detected between rounds.", ST["body"]))
        return story

    # ------------------------------------------------------------------
    # Page 4 — Surviving KQL Rules
    # ------------------------------------------------------------------

    def _page_kql_rules(self) -> list:
        ST = self.ST
        story = _section_header(
            "KQL RULES THAT SURVIVED",
            "Detection rules that caught at least one attack log",
        )

        if not self.scorer.surviving_kql:
            story.append(_p("No rules successfully detected any attack logs.", ST["body"]))
            return story

        story.append(_p(
            f"{len(self.scorer.surviving_kql)} rule(s) detected attack traffic. "
            "Severity is derived from the per-round detection rate.", ST["body"]))

        for i, rule in enumerate(self.scorer.surviving_kql, 1):
            sev_label, sev_color = _sev(rule["detection_rate"])
            sev_hex = _hex(sev_color)
            title_text = (
                f'Rule {i} — Round {rule["round"]}  '
                f'<font color="#{sev_hex}"><b>[{sev_label}]</b></font>  '
                f'· Detection rate: {rule["detection_rate"]:.0%}'
            )
            block = [
                Paragraph(title_text, ParagraphStyle(
                    "rule_title", fontName="Helvetica-Bold", fontSize=9,
                    textColor=_C["text"], spaceBefore=4*mm, spaceAfter=1*mm)),
                _code_block(rule["kql"]),
                Spacer(1, 2 * mm),
            ]
            story.append(KeepTogether(block))
        return story

    # ------------------------------------------------------------------
    # Page 5 — Detection Gaps
    # ------------------------------------------------------------------

    def _page_gaps(self) -> list:
        ST = self.ST
        story = _section_header(
            "DETECTION GAPS",
            "Fields never covered by any rule — persistent missed opportunities",
        )
        gaps = self.analyst._gap_fields()

        if not gaps:
            story.append(_p(
                "No persistent unaddressed signals found. "
                "The Defender's rules touched every consistently-present field. "
                "Evasion was caused by threshold or value mismatches, not blind spots.",
                ST["body"]))
            return story

        story.append(_p(
            "The following fields appeared in a high percentage of evaded logs "
            "across every round but were never referenced by any Defender rule. "
            "Each is a missed detection opportunity.", ST["body"]))

        hdr  = ["FIELD", "STABLE VALUE(S)", "EVADED LOG PRESENCE", "ROUNDS NEVER TARGETED"]
        rows = [hdr]
        for field, (values, pct) in gaps.items():
            vals = ", ".join(sorted(str(v) for v in values)[:3])
            if len(values) > 3:
                vals += f"  +{len(values)-3}"
            rows.append([field, vals, f"{pct:.0%}", f"all {len(self.scorer.rounds)}"])
        cw = [38*mm, _CONTENT_W - 101*mm, 28*mm, 35*mm]
        story.append(_col_table(rows, cw, small=True))

        # Recommended KQL for each gap field that has a remediation
        rem = _BattleAnalyst.FIELD_REMEDIATION
        covered = [f for f in gaps if f in rem]
        if covered:
            story.append(Spacer(1, 4 * mm))
            story.append(Paragraph("Recommended KQL Snippets", ST["section"]))
            story.append(_p(
                "These rules would directly address the gaps identified above.", ST["body"]))
            for field in covered:
                conf, title, kql_text = rem[field]
                story.append(KeepTogether([
                    _p(f"{field} — {title}", ST["cell_b"]),
                    Spacer(1, 1 * mm),
                    _code_block(kql_text),
                    Spacer(1, 3 * mm),
                ]))
        return story

