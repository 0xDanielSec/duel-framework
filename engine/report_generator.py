    # Page 6 — Sentinel Deployment Recommendations
    # ------------------------------------------------------------------

    def _page_recommendations(self) -> list:
        ST = self.ST
        story = _section_header(
            "SENTINEL DEPLOYMENT RECOMMENDATIONS",
            "Ordered by priority — High → Medium → Low",
        )
        story.append(_p(
            "Derived from fields that carried a stable, high-confidence attack signal "
            "across every round but were never addressed by any detection rule.",
            ST["body"]))

        gaps  = self.analyst._gap_fields()
        rem   = _BattleAnalyst.FIELD_REMEDIATION
        evaded_fields = set(self.analyst._all_evaded[0].keys()) if self.analyst._all_evaded else set()

        added: set[str] = set()
        buckets: dict[str, list] = {"High": [], "Medium": [], "Low": []}

        for field in gaps:
            if field in rem and field not in added:
                buckets[rem[field][0]].append((field, rem[field][1], rem[field][2], "gap"))
                added.add(field)
        for field in ("ConditionalAccessStatus", "AuthenticationRequirement"):
            if field not in added and field in rem:
                buckets[rem[field][0]].append((field, rem[field][1], rem[field][2], "structural"))
                added.add(field)
        for field, (conf, title, kql_text) in rem.items():
            if field not in added and field in evaded_fields:
                buckets[conf].append((field, title, kql_text, "supplemental"))
                added.add(field)

        if not any(buckets.values()):
            story.append(_p("No specific recommendations could be derived.", ST["body"]))
            return story

        origin_labels = {
            "gap":          "directly addresses a detected gap",
            "structural":   "structural control — baseline for any deployment",
            "supplemental": "supplemental — covers additional evasion surface",
        }

        rule_num = 0
        for conf in ("High", "Medium", "Low"):
            conf_color = _sev({"High": 0.9, "Medium": 0.55, "Low": 0.2}[conf])[1]
            for field, title, kql_text, origin in buckets[conf]:
                rule_num += 1
                pct = self.analyst._field_presence_pct(field, self.analyst._all_evaded)
                badge_hex = _hex(conf_color)
                heading = (
                    f'<font color="#{badge_hex}"><b>[{conf.upper()} PRIORITY]</b></font>'
                    f'  Rule {rule_num}: {title}'
                )
                rationale = (
                    f"`{field}` appeared in {pct:.0%} of evaded logs — "
                    f"this rule {origin_labels[origin]}."
                )
                story.append(KeepTogether([
                    Paragraph(heading, ParagraphStyle(
                        "rec_h", fontName="Helvetica-Bold", fontSize=9,
                        textColor=_C["text"], spaceBefore=5*mm, spaceAfter=1*mm)),
                    _p(f"Rationale: {rationale}", ST["body"]),
                    _code_block(kql_text),
                    Spacer(1, 2 * mm),
                ]))

        story += [
            Spacer(1, 5 * mm),
            Paragraph("Operational Notes", ST["section"]),
            _p(
                "• Tune count thresholds against your environment's baseline before enabling alerts.\n"
                "• Enrich IPAddress through ThreatIntelligenceIndicator for Tor/hosting-provider scoring.\n"
                "• Combine ConditionalAccessStatus == \"notApplied\" with AuthenticationRequirement == "
                "\"singleFactorAuthentication\" — both were stable signals throughout this battle.\n"
                "• Enable Entra ID Identity Protection to enforce risk-based CA policies that block "
                "suspicious logins at the authentication layer.",
                ST["body"]),
        ]
        return story

