def _section_header(title: str, subtitle: str = "") -> list:
    """Dark navy header block — used at the top of every section page."""
    ST = _styles()
    rows = [[Paragraph(title, ST["pg_title"])]]
    if subtitle:
        rows.append([Paragraph(subtitle, ST["pg_sub"])])
    t = Table(rows, colWidths=[_CONTENT_W])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, -1), _C["navy"]),
        ("TOPPADDING",    (0, 0), (-1, 0),  5 * mm),
        ("BOTTOMPADDING", (0, -1), (-1, -1), 4 * mm),
        ("LEFTPADDING",   (0, 0), (-1, -1), 4 * mm),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 4 * mm),
    ]))
    return [t, Spacer(1, 5 * mm)]


def _col_table(data: list[list], col_widths: list[float],
               header_bg=None, alt_rows=True, small: bool = False) -> Table:
    """Styled table with optional coloured header and alternating row shading."""
    header_bg = header_bg or _C["dark"]
    fs = 7 if small else 8
    # Wrap plain strings in data rows as Paragraphs so they word-wrap within cells
    wrapped = []
    for r_idx, row in enumerate(data):
        if r_idx == 0:
            wrapped.append(row)
        else:
            wrapped.append([
                _cp(cell, small=small) if isinstance(cell, str) else cell
                for cell in row
            ])
    t = Table(wrapped, colWidths=col_widths, repeatRows=1)
    style = [
        ("BACKGROUND",    (0, 0), (-1, 0),  header_bg),
        ("TEXTCOLOR",     (0, 0), (-1, 0),  white),
        ("FONTNAME",      (0, 0), (-1, 0),  "Helvetica-Bold"),
        ("FONTSIZE",      (0, 0), (-1, -1), fs),
        ("FONTNAME",      (0, 1), (-1, -1), "Helvetica"),
        ("TEXTCOLOR",     (0, 1), (-1, -1), _C["text"]),
        ("ALIGN",         (0, 0), (-1, 0),  "CENTER"),
        ("ALIGN",         (0, 1), (-1, -1), "LEFT"),
        ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
        ("TOPPADDING",    (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
        ("LEFTPADDING",   (0, 0), (-1, -1), 4),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 4),
        ("GRID",          (0, 0), (-1, -1), 0.4, _C["border"]),
    ]
    if alt_rows:
        for i in range(1, len(wrapped)):
            if i % 2 == 0:
                style.append(("BACKGROUND", (0, i), (-1, i), _C["alt"]))
    t.setStyle(TableStyle(style))
    return t


def _code_block(text: str) -> Table:
    """KQL/text in a light-grey monospace box with a coloured left border."""
    safe = html.escape(text.strip()).replace('\n', '<br/>')
    para = Paragraph(safe, ParagraphStyle(
        "code_wrap", fontName="Courier", fontSize=7,
        textColor=_C["text"], leading=10,
        leftIndent=4, rightIndent=4, wordWrap='LTR',
    ))
    t = Table([[para]], colWidths=[_CONTENT_W])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, -1), _C["code"]),
        ("LEFTPADDING",   (0, 0), (-1, -1), 4),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 4),
        ("TOPPADDING",    (0, 0), (-1, -1), 3 * mm),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3 * mm),
        ("LINEAFTER",     (0, 0), (-1, -1), 0, white),
        ("LINEBEFORE",    (0, 0), (-1, -1), 3, _C["blue"]),
        ("BOX",           (0, 0), (-1, -1), 0.4, _C["border"]),
    ]))
    return t


def _sev(rate: float) -> tuple[str, HexColor]:
    if rate >= 0.7:
        return "HIGH",   _C["red"]
    if rate >= 0.4:
        return "MEDIUM", _C["yellow"]
    return "LOW", _C["green"]


def _page_footer(canvas, doc):
    canvas.saveState()
    y = _MARGIN - 8 * mm
    canvas.setStrokeColor(_C["border"])
    canvas.setLineWidth(0.5)
    canvas.line(_MARGIN, y + 3.5 * mm, _PAGE_W - _MARGIN, y + 3.5 * mm)
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(_C["muted"])
    canvas.drawString(_MARGIN, y, _FOOTER)
    canvas.drawRightString(_PAGE_W - _MARGIN, y, f"Page {doc.page}")
    canvas.restoreState()


