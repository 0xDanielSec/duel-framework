class ReportGenerator:
    """
    Builds a professional PDF report from a completed BattleScorer instance.

    Usage:
        rg   = ReportGenerator(scorer)
        path = rg.generate()          # → Path to output/duel_report_*.pdf
    """

    def __init__(self, scorer: BattleScorer):
        self.scorer    = scorer
        self.tid       = scorer.technique_id
        self.tech_name = self._load_tech_name()
        self.analyst   = _BattleAnalyst(scorer.rounds, self.tid)
        self.ST        = _styles()
        self.winner    = self._winner()
        self.total_logs = sum(r["attack_log_count"] for r in scorer.rounds)
        self.total_det  = sum(r["detected_count"]   for r in scorer.rounds)
        self.det_rate   = self.total_det / self.total_logs if self.total_logs else 0.0
        self.eva_rate   = 1.0 - self.det_rate

    # ------------------------------------------------------------------
    # Public entry point
    # ------------------------------------------------------------------

    def generate(self) -> Path:
        """Build the PDF and return its path."""
        date_str = datetime.now(timezone.utc).strftime("%Y-%m-%d")
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        path = OUTPUT_DIR / f"duel_report_{self.tid}_{date_str}.pdf"

        doc = SimpleDocTemplate(
            str(path),
            pagesize=A4,
            leftMargin=_MARGIN, rightMargin=_MARGIN,
            topMargin=_MARGIN,  bottomMargin=_MARGIN + 10 * mm,
            title=f"DUEL Report — {self.tid}",
            author="DUEL Framework",
            subject=f"Adversarial detection analysis — {self.tid}",
        )
        story = (
            self._page_cover()
            + [PageBreak()]
            + self._page_statistics()
            + [PageBreak()]
            + self._page_mutations()
            + [PageBreak()]
            + self._page_kql_rules()
            + [PageBreak()]
            + self._page_gaps()
            + [PageBreak()]
            + self._page_recommendations()
        )
        doc.build(story, onFirstPage=_page_footer, onLaterPages=_page_footer)
        logger.info("PDF report saved to %s", path)
        return path

